skor_siswa = float(input('masukkan skor ='))

if skor_siswa>=40 :
    print (f'nilai {skor_siswa} = "E" ')
elif skor_siswa>=80 :
    print (f'nilai {skor_siswa} = "B" ')
elif skor_siswa>=70 :
    print (f'nilai {skor_siswa} = "C" ')
elif skor_siswa>=60 :
    print (f'nilai {skor_siswa} = "D" ')
elif skor_siswa>=90 :
    print (f'nilai {skor_siswa} = "A" ')
else :
    print (f'nilai {skor_siswa} = "F" ')